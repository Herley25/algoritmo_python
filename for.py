# LOOP FOR 

# O loop for em Python pode iterar pelos itens de qualquer sequência, como uma lista ou string.

#Sintaxe:

# for varuável in sequência:
#   comandos


# LOOP FOR COM CONTADOR 

# função range() - Gera uma lista contendo uma progressão aritmética.
# Sintaxe:
# range(inicio, fim, salto): 
# 
for x in range(1, 21):
    print(x)


for w in range(2, 11, 2):
    print(w)